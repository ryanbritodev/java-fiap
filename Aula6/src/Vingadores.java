// Criação da classe base

public class Vingadores{
    public String nome;
    public int idade;
    public boolean armadura;
    public double altura;

    // Metodo Construtor

    public Vingadores(String nome, int idade, boolean armadura, double altura){
        this.nome = nome;
        this.idade = idade;
        this.armadura = armadura;
        this.altura = altura;
    }
}